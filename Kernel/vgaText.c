static const uint8_t *glyph_for(char c) {
    if ((unsigned char)c < 32 || (unsigned char)c > 127) return font8x8_basic[0];
    return font8x8_basic[(unsigned char)c - 32];
}

void vga_draw_char(uint16_t x, uint16_t y, char c, uint8_t color) {
    const uint8_t *g = glyph_for(c);
    for (int row = 0; row < 8; row++) {
        uint8_t bits = g[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                vga_put_pixel(x + col, y + row, color);
            }
        }
    }
}

void vga_draw_string(uint16_t x, uint16_t y, const char *s, uint8_t color) {
    uint16_t cx = x;
    for (int i = 0; s[i] != 0; i++) {
        if (s[i] == '\n') {
            y += 8; cx = x;
        } else {
            vga_draw_char(cx, y, s[i], color);
            cx += 8;
        }
    }
}

void vga_text_input(uint16_t x, uint16_t y, char *buffer, uint16_t max_len, uint8_t color) {
    if (max_len == 0) return;
    uint16_t pos = 0;
    buffer[0] = 0;
    
    while (1) {
        char ch = readKeyAsciiBlocking();
        if (ch == 0) {
            continue; // non printable, ignore
        }
        if (ch == '\n' || ch == '\r') {
            // Enter: finish
            buffer[pos] = 0;
            break;
        }
        if ((ch == '\b' || ch == 127)) {
            // Backspace: erase one char
            if (pos > 0) {
                pos--;
                buffer[pos] = 0;
                // Draw a black box over the last char
                for (int dy = 0; dy < 8; dy++) {
                    for (int dx = 0; dx < 8; dx++) {
                        vga_put_pixel(x + pos*8 + dx, y + dy, 0);
                    }
                }
            }
            continue;
        }
        if (pos < max_len - 1) {
            buffer[pos++] = ch;
            buffer[pos] = 0;
            vga_draw_char(x + (pos-1)*8, y, ch, color);
        }
    }
}
